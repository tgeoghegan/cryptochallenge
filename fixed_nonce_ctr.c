int main(void) {
	int success = -1;
	char *key = NULL;
	char *plaintexts[sizeof(BASE64_PLAINTEXTS)];
	size_t plaintext_lens[sizeof(BASE64_PLAINTEXTS)];
	char *ciphertexts[sizeof(BASE64_PLAINTEXTS)];
	size_t ciphertext_lens[sizeof(BASE64_PLAINTEXTS)];
	const char nonce[BLOCKSIZE / 2] = { 0 };

	key = aes_generate_key();
	if (key == NULL) {
		print_fail("Fixed nonce CTR: failed to generate key");
		goto done;
	}

	for (size_t i = 0; i < sizeof(BASE64_PLAINTEXTS) / sizeof(BASE64_PLAINTEXTS[0]); i++) {
		if (!base64_to_raw(BASE64_PLAINTEXTS[i], strlen(BASE64_PLAINTEXTS[i]), &plaintexts[i], &plaintext_lens[i])) {
			print_fail("Fixed nonce CTR: failed to decode base64");
			goto done;
		}

		if (!aes_ctr(plaintexts[i], plaintext_lens[i], key, nonce, &ciphertexts[i], &ciphertext_lens[i])) {
			print_fail("Fixed nonce CTR: failed to encrypt");
			goto done;
		}

	}

	print_success("Fixed nonce CTR OK");
	success = 0;
done:
	free(key);
	for (size_t i = 0; i < sizeof(BASE64_PLAINTEXTS) / sizeof(BASE64_PLAINTEXTS[0]); i++) {
		free(plaintexts[i]);
		free(ciphertexts[i]);
	}

	return success;
}
